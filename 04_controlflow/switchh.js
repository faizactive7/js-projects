const month = 3

switch (month) {
    case 1:
        console.log("The Month is January");
        break;
    case 2:
        console.log("The Month is Feburary");
        break;
    case 3:
        console.log("The Month is March");
        break;
    case 4:
        console.log("The Month is April");
        break;

    default:
        console.log("Not In 4 Month");
        break;
}